            String sql ="UPDATE dokter SET nama = '"+FieldNamaDok.getText()+"', alamat = '"+FieldAlamat.getText()+"', jenis_kelamin= '"+jComboBox1.getSelectedItem()+"',tgl_lahir= '"+FieldTTL.getText()+"' WHERE kode_dokter = '"+FieldKodeDok.getText()+"'";
