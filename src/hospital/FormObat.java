    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String kode =jTable1.getValueAt(baris, 1).toString();
        FieldKodeObat.setText(kode);
        String nama = jTable1.getValueAt(baris,2).toString();
        FieldNamaObat.setText(nama);
        String stok = jTable1.getValueAt(baris, 3).toString();
        FieldStokObat.setText(stok);
        String harga = jTable1.getValueAt(baris, 4).toString();
        FieldHargaObat.setText(harga);
    }//GEN-LAST:event_jTable1MouseClicked

    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Kode Obat");
        model.addColumn("Nama Obat");
        model.addColumn("Stok Harga");
        model.addColumn("Harga Obat");
        
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from obat";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
        }
       
    }
    
